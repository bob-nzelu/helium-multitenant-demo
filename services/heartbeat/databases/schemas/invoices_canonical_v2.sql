CREATE TABLE invoice_line_items (
    id                          INTEGER PRIMARY KEY AUTOINCREMENT,

    -- UUID-style line ID for SDK compat (allows addressing lines without integer PK).
    line_id                     TEXT UNIQUE,

    -- Parent invoice (integer FK for performance).
    invoice_id                  INTEGER NOT NULL REFERENCES invoices(id) ON DELETE CASCADE,

    -- Display order within the invoice.
    line_number                 INTEGER NOT NULL,

    -- GOODS: physical product with HSN code.
    -- SERVICE: service with service code.
    -- Determines which classification fields are required below.
    line_item_type              TEXT NOT NULL DEFAULT 'GOODS'
                                    CHECK (line_item_type IN ('GOODS', 'SERVICE')),

    -- ── Common fields ──────────────────────────────────────────────────────
    -- description maps to FIRS invoice_line[].item.name (max 500 chars).
    description                 TEXT NOT NULL,
    quantity                    REAL NOT NULL CHECK (quantity > 0),
    unit_price                  REAL NOT NULL CHECK (unit_price >= 0),
    line_total                  REAL NOT NULL,        -- quantity × unit_price (pre-tax)
    tax_rate                    REAL DEFAULT 0.0,     -- e.g. 0.075 for 7.5% VAT
    tax_amount                  REAL DEFAULT 0.0,

    -- ── Goods classification (required when line_item_type = 'GOODS') ──────
    -- HS/HSN code in XXXX.XX format (6-digit with period, per FIRS standard).
    -- Maps to FIRS invoice_line[].hsn_code.
    hsn_code                    TEXT,

    -- Human-readable product category (from PDP classification).
    product_category            TEXT,

    -- ── Service classification (required when line_item_type = 'SERVICE') ──
    -- ISIC or FIRS service code.
    service_code                TEXT,

    -- Human-readable service category.
    service_category            TEXT,

    -- ── Product reference (application-level FK to inventory.db) ──────────
    product_id                  TEXT,
    -- Seller's internal SKU. Maps to FIRS sellers_item_identification.
    product_code                TEXT,
    product_name                TEXT,

    -- ── Audit self-containment (5 fields, v2.1.3.0) ──────────────────────
    -- Snapshot fields frozen at commit time. The line item carries enough
    -- context to be auditable without JOINing inventory/customer tables.

    -- Customer's own SKU for this product (from inventory.customer_sku).
    customer_sku                TEXT,

    -- OEM/manufacturer SKU (from inventory.oem_sku).
    oem_sku                     TEXT,

    -- Helium-assigned SKU (from inventory.helium_sku).
    helium_sku                  TEXT,

    -- Extended product/service description (from inventory.full_description).
    -- Longer than description; used in detailed audit views.
    full_description            TEXT,

    -- VAT rate as a percentage (e.g. 7.5 for 7.5%). Snapshot from inventory/tax config.
    -- Distinct from tax_rate which is the decimal form (0.075).
    vat_rate                    REAL,

    -- ── PDP classification intelligence ───────────────────────────────────
    -- Confidence score from PDP/LLM classification (0.0 – 1.0).
    classification_confidence   REAL,

    -- Source of the classification ('pdp', 'manual', 'llm', 'user_override').
    classification_source       TEXT,

    created_at                  TEXT NOT NULL DEFAULT (datetime('now')),

    UNIQUE (invoice_id, line_number)
);


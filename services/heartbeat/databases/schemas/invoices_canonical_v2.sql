-- ============================================================================
-- TABLE 1: invoices (130 fields incl. id)
-- Owner:  Core (authoritative writer)
-- Reads:  Edge (transmission_status updates via contract), Float SDK (read-only)
-- ============================================================================

CREATE TABLE invoices (

    -- ── A. Primary identification (10 fields) ────────────────────────────────

    id                          INTEGER PRIMARY KEY AUTOINCREMENT,

    -- Internal system identifier: 16-digit hex (e.g. A3F7B2C9D1E4F5A6).
    -- All internal API calls, logs, and cross-service references use this.
    invoice_id                  TEXT UNIQUE NOT NULL,

    -- Full display identifier: PRO-{TenantID}-{invoice_id}.
    -- Used in support contexts, UI labels, and FIRS cross-references.
    -- Immutable once committed. Generated by Core at COMMITTED transition.
    helium_invoice_no           TEXT UNIQUE NOT NULL,

    -- Business invoice number.
    -- OUTBOUND: client-generated (e.g. INV-2026-001, WN42752).
    -- INBOUND:  supplier's invoice number from the received document.
    -- This is the SWDB "Invoice No" column. Used to derive IRN.
    invoice_number              TEXT NOT NULL,

    -- Invoice Reference Number. Generated by HELIUM at commit/sign time.
    -- Format: INVOICE_NUMBER-SERVICE_ID-DATESTAMP (e.g. INV001-94ND90NR-20260215).
    -- Sent TO FIRS as part of the payload. FIRS validates it; they do NOT generate it.
    -- IDEMPOTENCY KEY: IRN serves as Helium's idempotency key. No two invoices
    -- share an IRN. FIRS uses it for deduplication. Every invoice MUST have an IRN
    -- at creation — there is no valid state without one.
    irn                         TEXT UNIQUE NOT NULL,

    -- Cryptographic Stamp Identifier. Generated by HELIUM at signing.
    -- NOT generated by FIRS.
    csid                        TEXT,
    -- PENDING | ISSUED | FAILED
    csid_status                 TEXT CHECK (csid_status IN ('PENDING', 'ISSUED', 'FAILED')),

    -- Core-generated UUID v7 — invoice-specific trace. Part of 3-level chain:
    -- user_trace_id (SDK) → x_trace_id (Relay) → invoice_trace_id (Core)
    invoice_trace_id            TEXT,

    -- SDK-generated UUID v7 from USER-TRACE block. Client origin trace.
    -- Generated on user gesture before any HTTP call. Propagated blob → invoice.
    user_trace_id               TEXT,

    -- Relay-generated UUID v7. Server authority trace.
    -- Generated after JWT validation. Propagates on all internal service calls.
    x_trace_id                  TEXT,

    -- SDK-generated UUID v7 at the finalize gesture time.
    -- Distinct from user_trace_id (upload time) — captures the commit action trace.
    finalize_trace_id           TEXT,

    -- Snapshot reference to the supplier/EIC config version active at commit time.
    -- Ensures reproducible re-processing if config changes post-submission.
    config_version_id           TEXT,

    -- Schema version in effect when this invoice was created. Immutable after insert.
    -- Links to schema_version.version for audit trail.
    schema_version_applied      TEXT,

    -- ── B. Three independent classifiers + FIRS type code (4 fields) ────────
    -- ALL required, ALL independent. Do NOT collapse.

    -- OUTBOUND = tenant is the seller (issuing the invoice).
    -- INBOUND  = tenant is the buyer (receiving the invoice).
    direction                   TEXT NOT NULL DEFAULT 'OUTBOUND'
                                    CHECK (direction IN ('OUTBOUND', 'INBOUND')),

    -- Document class. Independent of direction.
    document_type               TEXT NOT NULL DEFAULT 'COMMERCIAL_INVOICE'
                                    CHECK (document_type IN (
                                        'COMMERCIAL_INVOICE',
                                        'CREDIT_NOTE',
                                        'DEBIT_NOTE',
                                        'SELF_BILLED_INVOICE',
                                        'SELF_BILLED_CREDIT'
                                    )),

    -- FIRS invoice type code — raw numeric code sent in the FIRS payload.
    -- Stored alongside document_type for audit immutability.
    -- Mapping: COMMERCIAL_INVOICE→380, CREDIT_NOTE→381, DEBIT_NOTE→383,
    --          SELF_BILLED_INVOICE→389, SELF_BILLED_CREDIT→261.
    firs_invoice_type_code      TEXT,

    -- B2B | B2G | B2C — drives FIRS eligibility, tax rules, transmission path.
    transaction_type            TEXT NOT NULL DEFAULT 'B2B'
                                    CHECK (transaction_type IN ('B2B', 'B2G', 'B2C')),

    -- ── C. Dates (7 fields) ─────────────────────────────────────────────────

    -- Date on the invoice document (ISO date string: YYYY-MM-DD).
    issue_date                  TEXT NOT NULL,

    -- Optional time component of issue (HH:MM:SS).
    issue_time                  TEXT,

    -- Document-stated due date: what the invoice PDF/source document says.
    -- Higher authority — can trigger direct email alerts.
    -- SWDB column header: "Invoice Due Date"
    due_date                    TEXT,

    -- Platform-calculated payment due date: computed from issue_date + agreed credit terms.
    -- Used for internal SLA alerts only. May differ from due_date.
    -- SWDB column header: "Calculated Payment Due"
    payment_due_date            TEXT,

    -- When Helium signed the invoice (IRN + QR code generated). Pre-transmission.
    sign_date                   TEXT,

    -- When the invoice was actually sent to FIRS. Replaces old firs_submitted_at.
    transmission_date           TEXT,

    -- When the counterparty accepted the invoice.
    -- OUTBOUND: when the buyer acknowledged receipt/acceptance.
    -- INBOUND:  when this tenant accepted the supplier's invoice.
    acknowledgement_date        TEXT,

    -- ── D. Financial (7 fields) ─────────────────────────────────────────────

    -- Primary transaction currency (ISO 4217). Default NGN.
    document_currency_code      TEXT NOT NULL DEFAULT 'NGN',

    -- Tax computation currency. Usually same as document_currency_code.
    -- Separate field required for cross-currency B2B transactions.
    tax_currency_code           TEXT NOT NULL DEFAULT 'NGN',

    -- Pre-tax total. Maps to FIRS legal_monetary_total.line_extension_amount.
    -- tax_exclusive_amount = subtotal - allowances + charges (see invoice_allowance_charges).
    subtotal                    REAL NOT NULL DEFAULT 0.0,

    -- Total VAT/tax across all line items.
    tax_amount                  REAL NOT NULL DEFAULT 0.0,

    -- Grand total (subtotal + tax_amount).
    -- Maps to FIRS legal_monetary_total.tax_inclusive_amount AND payable_amount.
    total_amount                REAL NOT NULL DEFAULT 0.0,

    -- Exchange rate from document_currency_code to tax_currency_code.
    -- Required when the two currencies differ. NULL when same currency.
    exchange_rate               REAL,

    -- Whether this invoice has any discount/allowance entries (convenience flag).
    -- Detailed allowances stored in invoice_allowance_charges table.
    has_discount                INTEGER NOT NULL DEFAULT 0,

    -- Withholding tax amount (denormalized from invoice_tax_categories for display).
    -- NULL when no WHT applies. Maintained by application on commit/update.
    -- Allows SWDB "WHT ₦" column without JOIN to invoice_tax_categories.
    wht_amount                  REAL,

    -- Precise discount amount (denormalized from invoice_allowance_charges for display).
    -- NULL when no discount. has_discount is the boolean convenience; this is the value.
    discount_amount             REAL,

    -- Credit/debit note adjustment classification from Transforma/ingestion.
    -- Examples: PRICE_ADJUSTMENT, FULL_CANCELLATION, QUANTITY_CORRECTION, RETURN.
    -- NULL for standard commercial invoices.
    adjustment_type             TEXT,

    -- ── E. Payment means + FIRS code (2 fields) ─────────────────────────────
    -- How the buyer pays. Nullable (IBN — Include But Nullable).
    -- Human-readable; Edge maps to FIRS numeric codes at transmission
    -- (10=Cash, 20=Cheque, 30=Credit Transfer, 42=Payment to account, 8=Bank card).
    payment_means               TEXT CHECK (payment_means IS NULL OR payment_means IN (
                                    'CASH', 'CHEQUE', 'BANK_TRANSFER', 'CARD',
                                    'MOBILE_MONEY', 'DIGITAL_WALLET', 'OFFSET', 'OTHER'
                                )),

    -- FIRS payment means code — raw numeric code sent in the FIRS payload.
    -- Stored alongside payment_means for audit immutability.
    -- Mapping: CASH→10, CHEQUE→20, BANK_TRANSFER→30, CARD→48,
    --          MOBILE_MONEY→49, DIGITAL_WALLET→49, OFFSET→97, OTHER→1.
    firs_payment_means_code     TEXT,

    -- ── F. Delivery / fulfilment (2 fields) ─────────────────────────────────
    -- IBN fields — nullable, populated when applicable.

    delivery_date               TEXT,
    delivery_address            TEXT,

    -- ── G. Commercial references (2 fields) ─────────────────────────────────
    -- IBN fields — nullable, populated when applicable.

    -- Buyer's purchase order number. Critical for B2G transactions.
    purchase_order_number       TEXT,

    -- Contract/agreement number under which this invoice falls.
    contract_number             TEXT,

    -- ── H. Three-status model (4 fields) ────────────────────────────────────
    -- Three INDEPENDENT state machines. Do not conflate.

    -- High-level invoice lifecycle. Core is the ONLY authoritative writer.
    -- Users cannot update this field regardless of privilege.
    -- See: 02_WORKFLOW_STATUSES_V1.md for full state machine and transitions.
    workflow_status             TEXT NOT NULL DEFAULT 'COMMITTED'
                                    CHECK (workflow_status IN (
                                        'COMMITTED',
                                        'QUEUED',
                                        'TRANSMITTING',
                                        'TRANSMITTED',
                                        'VALIDATED',
                                        'ERROR',
                                        'ARCHIVED'
                                    )),

    -- Detailed outbound/inbound exchange progression.
    -- Core owns schema/domain. Edge writes through controlled contract.
    -- See: 03_TRANSMISSION_STATUSES_V1.md for full state machine.
    transmission_status         TEXT NOT NULL DEFAULT 'NOT_REQUIRED'
                                    CHECK (transmission_status IN (
                                        'NOT_REQUIRED',
                                        'PENDING_PRECHECK',
                                        'PRECHECK_PASSED',
                                        'BLOCKED_COUNTERPARTY',
                                        'SIGNING',
                                        'SIGNED',
                                        'TRANSMIT_PENDING',
                                        'TRANSMITTING',
                                        'TRANSMITTED',
                                        'ACCEPTED',
                                        'REJECTED',
                                        'FAILED_RETRYABLE',
                                        'FAILED_TERMINAL'
                                    )),

    -- Concise error summary for the most recent transmission failure.
    -- Cleared on successful retry. Full history in invoice_transmission_attempts.
    transmission_status_error   TEXT,

    -- Human-readable notes about current status. Set by Core when workflow/transmission
    -- transitions happen. Displayed in UI as contextual explanation (e.g. "Rejected by FIRS:
    -- invalid TIN", "Queued for retry", "Payment confirmed by buyer").
    status_notes                TEXT,

    -- Payment lifecycle. This is the ONLY status field users can update (permission-gated).
    -- OVERDUE and VOID are DISPLAY-DERIVED states — computed at runtime, NOT persisted.
    -- OVERDUE = UNPAID + due_date < today
    -- VOID    = workflow_status = ARCHIVED + payment_status = UNPAID
    payment_status              TEXT NOT NULL DEFAULT 'UNPAID'
                                    CHECK (payment_status IN (
                                        'UNPAID',
                                        'PAID',
                                        'PARTIAL',
                                        'DISPUTED',
                                        'CANCELLED'
                                    )),

    -- Timestamp of the last payment_status change (ISO datetime).
    payment_updated_at          TEXT,

    -- User email or system identifier who changed payment_status.
    payment_updated_by          TEXT,

    -- ── I. Retry mechanics (3 fields) ───────────────────────────────────────

    -- Total transmission retries attempted across all attempts.
    retry_count                 INTEGER NOT NULL DEFAULT 0,

    -- Timestamp of most recent retry attempt (ISO datetime).
    last_retry_at               TEXT,

    -- Scheduled timestamp for next retry (ISO datetime). NULL = no retry scheduled.
    next_retry_at               TEXT,

    -- ── J. FIRS audit artefacts (3 fields) ──────────────────────────────────
    -- Concrete FIRS API response artefacts.
    -- transmission_status is the abstracted platform state; these are the raw receipts.

    -- FIRS confirmation/acknowledgement number.
    firs_confirmation           TEXT,

    -- Full FIRS API response payload (JSON blob). For audit and re-submission.
    firs_response_data          TEXT,

    -- Complete FIRS submission record (JSON). Stores BOTH:
    -- • outbound_payload: what was sent to FIRS (the request body)
    -- • inbound_payload:  what FIRS returned (full response)
    -- Written at transmission time. Immutable after first write.
    firs_submitted_payload      TEXT,

    -- QR code data generated by HELIUM at signing. Contains verification fingerprint.
    -- Rendered in invoice PDF. NOT generated by FIRS.
    qr_code_data                TEXT,

    -- ── K. Company / seller party (14 fields) ───────────────────────────────
    -- The SELLER is the party issuing the invoice.
    --   • For OUTBOUND:                  seller = TENANT (the Helium platform company)
    --   • For INBOUND / SELF_BILLED_*:   seller = COUNTERPARTY (the supplier)
    --
    -- company_id always identifies the Helium tenant regardless of direction.
    -- seller_* fields are audit-stable snapshots at commit time.
    --
    -- FIRS MAPPING: seller_* → accounting_supplier_party.*
    -- seller_address = street_name, seller_city = city_name (separate FIRS mandatory fields).

    -- Helium tenant identifier. NOT NULL — every invoice belongs to a tenant.
    company_id                  TEXT NOT NULL,

    -- Seller party record ID.
    -- OUTBOUND: tenant's own record identifier.
    -- INBOUND:  counterparty's record from customers.db.
    seller_id                   TEXT,

    -- FIRS-registered Business ID for the seller. Immutable snapshot at commit time.
    -- Top-level field in FIRS payload: { "business_id": "..." }.
    -- Separate from TIN/Tax ID — this is the FIRS platform registration identifier.
    -- Stored per-invoice for audit: if business_id changes, old invoices retain the value
    -- that was active at the time of creation/submission.
    seller_business_id          TEXT,

    seller_name                 TEXT,

    -- Legacy TIN (########-####). One of three accepted FIRS tax identifiers.
    seller_tin                  TEXT,

    -- FIRS New 13-digit Tax ID. One of three accepted FIRS tax identifiers.
    seller_tax_id               TEXT,

    -- CAC RC Number (13 digits). One of three accepted FIRS tax identifiers.
    seller_rc_number            TEXT,

    seller_email                TEXT,

    -- Phone with country code (e.g. +2348012345678).
    seller_phone                TEXT,

    -- Street address (free-text). Maps to FIRS postal_address.street_name (max 200 chars).
    seller_address              TEXT,

    -- City or town name. Maps to FIRS postal_address.city_name (max 100 chars).
    -- Separate field because FIRS requires city_name as a mandatory standalone value.
    seller_city                 TEXT,

    seller_postal_code          TEXT,

    -- FIRS LGA code (1–774). Validated at application layer, not CHECK.
    seller_lga_code             TEXT,

    -- FIRS state code (1–37: 36 states + FCT).
    seller_state_code           TEXT,

    -- ISO 3166-1 alpha-2 country code. Default 'NG' for Nigeria.
    seller_country_code         TEXT DEFAULT 'NG',

    -- ── L. Buyer party (13 fields) ──────────────────────────────────────────
    -- The BUYER is the party receiving/paying for the invoice.
    --   • For OUTBOUND:                  buyer = COUNTERPARTY (the customer)
    --   • For INBOUND / SELF_BILLED_*:   buyer = TENANT (the Helium platform company)
    --
    -- buyer_* fields are audit-stable snapshots at commit time.
    -- Frontend always displays the counterparty side as "Customer" regardless
    -- of direction. For OUTBOUND → reads buyer_name. For INBOUND → reads seller_name.
    --
    -- FIRS MAPPING: buyer_* → accounting_customer_party.*
    --   (conditional mandatory for B2B: tin, email, postal_address required)
    -- buyer_address = street_name, buyer_city = city_name (separate FIRS mandatory fields).

    buyer_id                    TEXT,

    -- FIRS-registered Business ID for the buyer. Immutable snapshot at commit time.
    -- Top-level field in FIRS payload: { "business_id": "..." }.
    -- Separate from TIN/Tax ID — this is the FIRS platform registration identifier.
    -- Stored per-invoice for audit: if business_id changes, old invoices retain the value
    -- that was active at the time of creation/submission.
    buyer_business_id           TEXT,

    buyer_name                  TEXT,

    -- Legacy TIN (########-####).
    buyer_tin                   TEXT,

    -- FIRS New 13-digit Tax ID.
    buyer_tax_id                TEXT,

    -- CAC RC Number (13 digits).
    buyer_rc_number             TEXT,

    buyer_email                 TEXT,

    -- Phone with country code.
    buyer_phone                 TEXT,

    -- Street address (free-text). Maps to FIRS postal_address.street_name (max 200 chars).
    buyer_address               TEXT,

    -- City or town name. Maps to FIRS postal_address.city_name (max 100 chars).
    buyer_city                  TEXT,

    buyer_postal_code           TEXT,

    -- FIRS LGA code (1–774).
    buyer_lga_code              TEXT,

    -- FIRS state code (1–37).
    buyer_state_code            TEXT,

    -- ISO 3166-1 alpha-2 country code.
    buyer_country_code          TEXT DEFAULT 'NG',

    -- ── M. User (4 fields) ──────────────────────────────────────────────────


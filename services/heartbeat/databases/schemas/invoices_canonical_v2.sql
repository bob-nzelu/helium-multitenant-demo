-- Purpose: One invoice can have MULTIPLE tax categories applied across its
--          line items. This table stores the per-category tax breakdown.
--          Tax category codes match the FIRS/NRS API enum (24 values + NEPZA + WITHHOLDING_TAX).
--          Maps to: tax_total[].tax_subtotal[].tax_category in FIRS payload.
-- ============================================================================

CREATE TABLE invoice_tax_categories (
    id                          INTEGER PRIMARY KEY AUTOINCREMENT,

    invoice_id                  INTEGER NOT NULL REFERENCES invoices(id) ON DELETE CASCADE,

    -- FIRS/NRS tax category code (26 values: 24 FIRS API + NEPZA + WITHHOLDING_TAX).
    tax_category                TEXT NOT NULL
                                    CHECK (tax_category IN (
                                        'STANDARD_GST',
                                        'REDUCED_GST',
                                        'ZERO_GST',
                                        'STANDARD_VAT',
                                        'REDUCED_VAT',
                                        'ZERO_VAT',
                                        'STATE_SALES_TAX',
                                        'LOCAL_SALES_TAX',
                                        'ALCOHOL_EXCISE_TAX',
                                        'TOBACCO_EXCISE_TAX',
                                        'FUEL_EXCISE_TAX',
                                        'CORPORATE_INCOME_TAX',
                                        'PERSONAL_INCOME_TAX',
                                        'SOCIAL_SECURITY_TAX',
                                        'MEDICARE_TAX',
                                        'REAL_ESTATE_TAX',
                                        'PERSONAL_PROPERTY_TAX',
                                        'CARBON_TAX',
                                        'PLASTIC_TAX',
                                        'IMPORT_DUTY',
                                        'EXPORT_DUTY',
                                        'LUXURY_TAX',
                                        'SERVICE_TAX',
                                        'TOURISM_TAX',
                                        'NEPZA',
                                        'WITHHOLDING_TAX'
                                    )),

    -- Applicable tax rate for this category as decimal (e.g. 0.075 for 7.5%).
    -- Edge multiplies by 100 to send percent to FIRS (e.g. 7.5).
    tax_rate                    REAL NOT NULL DEFAULT 0.075,

    -- Sum of taxable line item amounts under this category.
    taxable_amount              REAL NOT NULL DEFAULT 0.0,

    -- Total tax computed for this category (taxable_amount × tax_rate).
    tax_amount                  REAL NOT NULL DEFAULT 0.0,

    created_at                  TEXT NOT NULL DEFAULT (datetime('now')),

    UNIQUE (invoice_id, tax_category)
);

CREATE INDEX idx_tax_cat_invoice_id     ON invoice_tax_categories(invoice_id);

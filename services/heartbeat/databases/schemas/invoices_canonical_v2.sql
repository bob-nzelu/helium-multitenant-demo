-- Trigger: inbound_status change tracking
CREATE TRIGGER trg_audit_inbound_status
AFTER UPDATE OF inbound_status ON invoices
WHEN OLD.inbound_status IS NOT NEW.inbound_status
     OR (OLD.inbound_status IS NULL AND NEW.inbound_status IS NOT NULL)
BEGIN
    INSERT INTO invoice_history (
        invoice_id, change_type, changed_field, old_value, new_value,
        changed_by, change_reason
